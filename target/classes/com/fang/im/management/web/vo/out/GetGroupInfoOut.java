package com.fang.im.management.web.vo.out;

import java.util.List;

/**
 * 解析im接口返回的群信息
 *
 * @author zhaozele
 */
public class GetGroupInfoOut {

  /**
   * 命令
   */
  private String command;

  /**
   * 信息集合
   */
  private List<ImUserServiceInterfaceUrlOut> data;

  /**
   * 结果总数
   */
  private String msg;

  /**
   * 返回码
   */
  private String retCode;

  /**
   * 获取命令
   *
   * @return 命令
   */
  public String getCommand() {
    return command;
  }

  /**
   * 设置命令
   *
   * @param command
   *        命令
   */
  public void setCommand(String command) {
    this.command = command;
  }

  /**
   * 获取数据
   *
   * @return 数据
   */
  public List<ImUserServiceInterfaceUrlOut> getData() {
    return data;
  }

  /**
   * 设置数据
   *
   * @param data
   *        数据
   */
  public void setData(List<ImUserServiceInterfaceUrlOut> data) {
    this.data = data;
  }

  /**
   * 获取总数
   *
   * @return 总数
   */
  public String getMsg() {
    return msg;
  }

  /**
   * 设置总数
   *
   * @param msg
   *        总数
   */
  public void setMsg(String msg) {
    this.msg = msg;
  }

  /**
   * 设置返回码
   *
   * @return 返回码
   */
  public String getRetCode() {
    return retCode;
  }

  /**
   * 设置返回码
   *
   * @param retCode
   *        返回码
   */
  public void setRetCode(String retCode) {
    this.retCode = retCode;
  }
}
