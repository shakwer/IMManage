package com.fang.im.management.po;

import java.io.Serializable;
import java.util.Date;
import java.util.Set;

/**
 * <p>
 * Description: SysUser
 * </p>
 *
 * @author jinshilei
 *         2017年4月6日
 * @version 1.0
 *
 */
public class SysUserResource implements Serializable {

  /**
   * serialVersionUID
   */
  private static final long serialVersionUID = -4958991653203176031L;

  /**
   * 用户编号
   */
  private Integer sysUserId;

  /**
   * 用户邮箱
   */
  private String email;

  /**
   * OA用户在im的用户名(目前支持的是app端的前缀)
   */
  private String imUserName;

  /**
   * 用户真实姓名
   */
  private String sysUserRealName;

  /**
   * 角色
   */
  private SysRole sysRole;

  /**
   * 城市
   */
  private Set<SysCity> sysCities;

  /**
   * 开通权限时间
   */
  private Date createTime;

  /**
   * 用户的最后登录时间
   */
  private Date lastLoginTime;

  /**
   * 是否删除
   */
  private Boolean isDelete;

  /**
   *
   * 构造函数
   */
  public SysUserResource() {
  }

  /**
   *
   * getSysUserId
   *
   * @return 返回值
   */
  public Integer getSysUserId() {
    return sysUserId;
  }

  /**
   *
   * setSysUserId
   *
   * @param sysUserId
   *        用户id
   */
  public void setSysUserId(Integer sysUserId) {
    this.sysUserId = sysUserId;
  }

  /**
   *
   * getEmail
   *
   * @return 返回值
   */
  public String getEmail() {
    return email;
  }

  /**
   *
   * setEmail
   *
   * @param email
   *        邮箱
   */
  public void setEmail(String email) {
    this.email = email;
  }

  /**
   *
   * getImUserName
   *
   * @return 返回值
   */
  public String getImUserName() {
    return imUserName;
  }

  /**
   *
   * setImUserName
   *
   * @param imUserName
   *        im用户名
   */
  public void setImUserName(String imUserName) {
    this.imUserName = imUserName;
  }

  /**
   *
   * getSysRole
   *
   * @return 返回值
   */
  public SysRole getSysRole() {
    return sysRole;
  }

  /**
   *
   * setSysRole
   *
   * @param sysRole
   *        角色
   */
  public void setSysRole(SysRole sysRole) {
    this.sysRole = sysRole;
  }

  /**
   *
   * getSysCities
   *
   * @return 返回值
   */
  public Set<SysCity> getSysCities() {
    return sysCities;
  }

  /**
   *
   * setSysCities
   *
   * @param sysCities
   *        城市
   */
  public void setSysCities(Set<SysCity> sysCities) {
    this.sysCities = sysCities;
  }

  /**
   *
   * getSysUserRealName
   *
   * @return 返回值
   */
  public String getSysUserRealName() {
    return sysUserRealName;
  }

  /**
   *
   * setSysUserRealName
   *
   * @param sysUserRealName
   *        真实姓名
   */
  public void setSysUserRealName(String sysUserRealName) {
    this.sysUserRealName = sysUserRealName;
  }

  /**
   *
   * getCreateTime
   *
   * @return 返回值
   */
  public Date getCreateTime() {
    return createTime;
  }

  /**
   *
   * setCreateTime
   *
   * @param createTime
   *        创建时间
   */
  public void setCreateTime(Date createTime) {
    this.createTime = createTime;
  }

  /**
   *
   * getLastLoginTime
   *
   * @return 返回值
   */
  public Date getLastLoginTime() {
    return lastLoginTime;
  }

  /**
   *
   * setLastLoginTime
   *
   * @param lastLoginTime
   *        最后登录时间
   */
  public void setLastLoginTime(Date lastLoginTime) {
    this.lastLoginTime = lastLoginTime;
  }

  /**
   *
   * getIsDelete
   *
   * @return 返回值
   */
  public Boolean getIsDelete() {
    return isDelete;
  }

  /**
   *
   * setIsDelete
   *
   * @param isDelete
   *        是否有效
   */
  public void setIsDelete(Boolean isDelete) {
    this.isDelete = isDelete;
  }
}
